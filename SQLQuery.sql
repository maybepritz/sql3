-- -------------------------------------------------------
--  Триггер RESTRICT_TRG
--  Запрещает удаление отдела, в котором есть сотрудники.
-- -------------------------------------------------------
CREATE TRIGGER RESTRICT_TRG
ON Отдел
FOR DELETE
AS
BEGIN
    -- Если хотя бы один сотрудник ссылается на удаляемый отдел — откат
    IF EXISTS (
        SELECT 1
        FROM   Сотрудник2 AS С
        JOIN   deleted    AS d ON С.Код_отдела = d.Код_отдела
    )
    BEGIN
        RAISERROR('Удаление запрещено: в отделе есть сотрудники!', 16, 10);
        ROLLBACK TRAN;
    END
    RETURN;
END;
GO


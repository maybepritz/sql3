-- -------------------------------------------------------
--  Процедура ВывестиЗП
--  Параметры:
--    @Фамилия  — входной (поиск по LIKE)
--    @Зарплата — выходной (возвращает сумму)
--    @Дата     — входной с дефолтным значением
-- -------------------------------------------------------
CREATE PROCEDURE ВывестиЗП (
    @Фамилия  VARCHAR(50),
    @Зарплата MONEY OUTPUT,
    @Дата     DATE = '2026-03-24'
)
AS
BEGIN
    SELECT @Зарплата = НЗ.Итого_зарплаты
    FROM   Начисление_зарплаты AS НЗ
    JOIN   Сотрудник           AS С  ON С.Табельный_номер = НЗ.Табельный_номер
    WHERE  ФИО             LIKE @Фамилия + '%'
      AND  НЗ.Дата_зарплаты   = @Дата;
END;
GO

-- Позиционный вызов (дата — дефолтная)
DECLARE @РезультатЗП MONEY;
EXEC ВывестиЗП 'Сидоров', @РезультатЗП OUTPUT;
PRINT @РезультатЗП;

-- Именованный вызов с явной датой
DECLARE @РезультатЗП MONEY;
EXEC ВывестиЗП
    @Фамилия  = 'Сидоров',
    @Дата     = '2025-12-25',
    @Зарплата = @РезультатЗП OUTPUT;
PRINT @РезультатЗП;

-- Смешанный вызов (первый аргумент позиционный, второй именованный)
DECLARE @РезультатЗП MONEY;
EXEC ВывестиЗП 'Сидоров', @Зарплата = @РезультатЗП OUTPUT;
PRINT @РезультатЗП;
GO


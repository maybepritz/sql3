-- Штатное расписание
CREATE TABLE Штатное_расписание (
    Код_должности        INT          NOT NULL PRIMARY KEY,
    Название_должности   VARCHAR(50)  NOT NULL UNIQUE,
    Количество_вакансий  SMALLINT     NOT NULL,
    Количество_занятых   SMALLINT     NOT NULL,
    Оклад                MONEY        NOT NULL,

    -- Вакансий и занятых мест не может быть отрицательным числом
    CHECK (Количество_вакансий >= 0),
    CHECK (Количество_занятых  >= 0),

    -- Занятых мест не может быть больше, чем вакансий
    CHECK (Количество_занятых  <= Количество_вакансий)
);
GO

-- Справочник налогов
CREATE TABLE Налог (
    Код_налога  INT         NOT NULL PRIMARY KEY,
    Вид_налога  VARCHAR(50) NOT NULL UNIQUE,
    Ставка      MONEY       NOT NULL
);
GO

-- Справочник надбавок
CREATE TABLE Надбавка (
    Код_надбавки      INT         NOT NULL PRIMARY KEY,
    Название_надбавки VARCHAR(50) NOT NULL UNIQUE,
    Ставка            MONEY       NOT NULL
);
GO

-- Сотрудники
CREATE TABLE Сотрудник (
    Табельный_номер INT          NOT NULL PRIMARY KEY,
    ФИО             VARCHAR(100) NOT NULL,
    Код_должности   INT          NOT NULL FOREIGN KEY REFERENCES Штатное_расписание(Код_должности),
    Разряд          TINYINT      NOT NULL,
    Наличие_льгот   BIT          NOT NULL   -- 1 = есть льготы, 0 = нет
);
GO

-- Начисления зарплаты
CREATE TABLE Начисление_зарплаты (
    Код_начисления  INT  NOT NULL PRIMARY KEY,
    Табельный_номер INT  NOT NULL FOREIGN KEY REFERENCES Сотрудник(Табельный_номер),
    Дата_зарплаты   DATE NOT NULL,
    Итого_зарплаты  MONEY NOT NULL
);
GO

-- Суммы налогов по каждому начислению зарплаты
CREATE TABLE Начисление_налога (
    Код_налога      INT   NOT NULL FOREIGN KEY REFERENCES Налог(Код_налога),
    Код_начисления  INT   NOT NULL FOREIGN KEY REFERENCES Начисление_зарплаты(Код_начисления),
    Сумма_налога    MONEY NOT NULL,
    CONSTRAINT PK_начисление_налога PRIMARY KEY (Код_налога, Код_начисления)
);
GO

-- Суммы надбавок по каждому начислению зарплаты
CREATE TABLE Начисление_надбавки (
    Код_надбавки    INT   NOT NULL FOREIGN KEY REFERENCES Надбавка(Код_надбавки),
    Код_начисления  INT   NOT NULL FOREIGN KEY REFERENCES Начисление_зарплаты(Код_начисления),
    Сумма_надбавки  MONEY NOT NULL,
    CONSTRAINT PK_начисление_надбавки PRIMARY KEY (Код_надбавки, Код_начисления)
);
GO



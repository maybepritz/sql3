-- -------------------------------------------------------
--  Триггер unique_name
--  Запрещает INSERT/UPDATE с ФИО, которое уже есть в таблице.
-- -------------------------------------------------------
CREATE TRIGGER unique_name
ON Сотрудник2
FOR UPDATE, INSERT
AS
BEGIN
    -- Проверяем только при изменении столбца ФИО
    IF UPDATE(ФИО)
       AND (SELECT COUNT(*)
            FROM   Сотрудник2 AS С
            JOIN   INSERTED   AS I ON С.ФИО = I.ФИО) > 1
    BEGIN
        RAISERROR('Изменение запрещено: сотрудник с таким ФИО уже есть!', 16, 10);
        ROLLBACK TRAN;
    END
    RETURN;
END;
GO

-- INSERT с уникальным ФИО — пройдёт успешно
INSERT INTO Сотрудник2 VALUES (5, 'Новиков Николай', 2);

-- INSERT с дублирующим ФИО — будет заблокирован триггером
INSERT INTO Сотрудник2 VALUES (6, 'Иванов Иван', 1);

-- UPDATE на уникальное ФИО — пройдёт успешно
UPDATE Сотрудник2 SET ФИО = 'Морозов Максим' WHERE Табельный_номер = 4;

-- UPDATE на уже существующее ФИО — будет заблокирован триггером
UPDATE Сотрудник2 SET ФИО = 'Иванов Иван' WHERE Табельный_номер = 3;